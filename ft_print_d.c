#include "ft_printf.h"

int	ft_print_d(int n)
{
	long		i;
	size_t		d;

	i = n;
	d = 0;
	if (i < 0)
	{
		i *= -1;
		d += write(1, "-", 1);
	}
	if (i >= 10)
		d += ft_print_d(i / 10);
	return (d + write(1, &((char){i % 10 + '0'}), 1));
}
