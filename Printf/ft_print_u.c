#include "ft_printf.h"

int	ft_print_u(unsigned int u)
{
	size_t	d;

	d = 0;
	if (u >= 10)
		d += ft_print_u(u / 10);
	return (d + write(1, &((char){u % 10 + '0'}), 1));
}
